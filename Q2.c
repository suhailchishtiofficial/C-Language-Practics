// 2. Arithmetic Operations

#include<stdio.h>
int main(){
    int a , b;
    a=10;
    b=7;

    printf("Sum of %d & %d is %d\n",a,b, a+b);
    printf("Substraction of %d & %d is %d\n",a,b,a-b);
    printf("Product of %d & %d is %d\n",a,b,a*b);
    printf("Division of %d & %d is %d\n",a,b,a/b);    

    return 0;
}